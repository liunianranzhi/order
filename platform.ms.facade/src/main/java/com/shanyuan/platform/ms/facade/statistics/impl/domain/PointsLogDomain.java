package com.shanyuan.platform.ms.facade.statistics.impl.domain;

public class PointsLogDomain {

	private String pointsAddTime;
	private Integer points;
	private String desc;
	public String getPointsAddTime() {
		return pointsAddTime;
	}
	public void setPointsAddTime(String pointsAddTime) {
		this.pointsAddTime = pointsAddTime;
	}
	public Integer getPoints() {
		return points;
	}
	public void setPoints(Integer points) {
		this.points = points;
	}
	public String getDesc() {
		return desc;
	}
	public void setDesc(String desc) {
		this.desc = desc;
	}
	
}
